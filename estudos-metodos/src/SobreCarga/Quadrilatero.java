package SobreCarga;

public class Quadrilatero {
     public static void area (int base, int altura){

        System.out.println("A area do quadrado é:" + base * altura);

     }

     public static void area (int baseMaior, int baseMenor, int alturaTrapezio){

        System.out.println("A area do quadrado é:" + ((baseMaior + baseMenor)*alturaTrapezio) /2);

     }
}
